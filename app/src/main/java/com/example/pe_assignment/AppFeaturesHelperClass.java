public class AppFeaturesHelperClass {

    int image;
    String title;

    public AppFeaturesHelperClass(int image, String title) {
        this.image = image;
        this.title = title;
    }

    public int getImage() {
        return image;
    }

    public String getTitle() {
        return title;
    }
}
